package api.products;

/**
 * The Class Author.
 * 
 * @author Gwindow
 */
public class Author {
	
	/** The account id. */
	private String accountId;
	
	/** The name. */
	private String name;

	/**
	 * Gets the account id.
	 * 
	 * @return the account id
	 */
	public String getAccountId() {
		return this.accountId;
	}

	/**
	 * Sets the account id.
	 * 
	 * @param accountId
	 *            the new account id
	 */
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}

	/**
	 * Gets the name.
	 * 
	 * @return the name
	 */
	public String getName() {
		return this.name;
	}

	/**
	 * Sets the name.
	 * 
	 * @param name
	 *            the new name
	 */
	public void setName(String name) {
		this.name = name;
	}
}
