
package api.products;

/**
 * The Class Images.
 * 
 * @author Gwindow
 */
public class Images {

	/** The link. */
	private String link;

	/**
	 * Gets the link.
	 * 
	 * @return the link
	 */
	public String getLink() {
		return this.link;
	}

	/**
	 * Sets the link.
	 * 
	 * @param link
	 *            the new link
	 */
	public void setLink(String link) {
		this.link = link;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Images [link=" + link + "]";
	}
}
